export function getToolColor(name: string): string {
  const colors = ['#f5c518', '#e5e7eb', '#9ca3af', '#6b7280', '#f59e0b', '#fbbf24', '#d1d5db', '#e9d5ff', '#bfdbfe', '#bbf7d0']
  let hash = 0
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash)
  }
  return colors[Math.abs(hash) % colors.length]
}

export function getInitials(name: string): string {
  const chinese = /[一-鿿]/
  if (chinese.test(name)) return name.slice(0, 2)
  const words = name.split(/[\s-_]+/)
  if (words.length >= 2) return (words[0][0] + words[1][0]).toUpperCase()
  return name.slice(0, 2).toUpperCase()
}

export function getToolBySlug(slug: string): AITool | undefined {
  return tools.find(t => t.slug === slug)
}

export function getToolsByCategory(category: CategorySlug): AITool[] {
  return tools.filter(t => t.categories.includes(category))
}

export function getCategoryName(slug: CategorySlug): string {
  return CATEGORIES.find(c => c.slug === slug)?.name ?? slug
}
